package ex_072124;

public class Dowhile {
    public static void main(String[] args) {
        int i = 0;
        do{
            System.out.println(i);
            i++;
        }while(i < 10);

        int j=10;
        do{
            System.out.println(j);//run atleast once
            j++;

        }while(j < 10);


        int k=10;
        while(k < 10){
            System.out.println(k);
            k++;
        }

    }
}
